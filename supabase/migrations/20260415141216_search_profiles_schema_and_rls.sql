-- 6) Enriched search view (no availability projection in this phase).
create or replace view public.professional_search_cards_enriched
with (security_invoker = true) as
select
  u.id as professional_id,
  u.first_name,
  u.last_name,
  u.profile_photo_url,
  u.bio,
  u.country_code,
  u.location_locality,
  u.location_region,
  u.postal_code,
  u.service_area,
  u.rating_avg,
  u.rating_count,
  coalesce(
    array_agg(distinct s.label) filter (where s.label is not null),
    array[]::text[]
  ) as specialties
from public.users u
left join public.professional_specialties ps
  on ps.professional_id = u.id
left join public.specialties s
  on s.id = ps.specialty_id
where u.is_professional = true
  and u.is_profile_complete = true
  and u.is_searchable = true
  and u.is_public_searchable = true
group by
  u.id,
  u.first_name,
  u.last_name,
  u.profile_photo_url,
  u.bio,
  u.country_code,
  u.location_locality,
  u.location_region,
  u.postal_code,
  u.service_area,
  u.rating_avg,
  u.rating_count;

grant select on public.professional_search_cards_enriched to anon, authenticated;
